import matchers from '../task2';

test('matchers', () => {
  const user1 = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const userSorted = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  const sort = matchers(user1);
  expect(sort).toEqual(userSorted);
});